#[cfg(test)]
mod tests {
    // 부모 스코프의 solve 함수를 가져와 사용 가능하도록 처리합니다.
    use super::*;

    // 테스트 프레임워크가 이를 실행 테스트 케이스로 인식하게 만듭니다.
    #[test]
    fn test_solve() {
        // 괄호가 교대로 들어간 정상 괄호 배열을 인자로 넘겨 solve 함수를 실행합니다.
        let ans = solve(vec!['(', ')', '(', ')', '(', ')', '(', ')', '(', ')']);
        // 반환 결과인 ans가 true인지 확인하여 맞지 않다면 테스트 실패를 일으킵니다.
        assert_eq!(ans, true);
    }
}
