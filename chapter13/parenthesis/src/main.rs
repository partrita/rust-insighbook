// 괄호쌍의 짝이 올바르게 맞는지 판별하는 함수입니다.
// s는 유니코드 문자 타입(char)의 벡터를 소유권 형태로 받습니다.
// 올바른 괄호 문자열이면 true를, 그렇지 않으면 false를 반환합니다.
fn solve(s: Vec<char>) -> bool {
    // 입력받은 문자 벡터의 길이를 구하여 변수 n에 담습니다.
    let n = s.len();

    // 아직 닫는 괄호 ')'를 만나지 못해 대기 상태인 여는 괄호 '('의 개수를 세기 위해
    // 가변 정수 변수 left를 0으로 초기화합니다.
    // 정상적으로 대칭이 맞다면 최종적으로 left의 값은 0이어야 합니다.
    let mut left = 0;

    // 문자열의 첫 번째 글자(인덱스 0)부터 마지막 글자(인덱스 n-1)까지 차례대로 순회합니다.
    for i in 0..n {
        // 현재 인덱스의 문자가 여는 괄호 '('인 경우
        if s[i] == '(' {
            // 여는 괄호 개수 카운터인 left를 1 더합니다.
            left += 1;
        } else {
            // 현재 문자가 닫는 괄호 ')'인 경우
            // 만약 대기 중인 여는 괄호가 전혀 없다면(left == 0),
            // 짝이 맞지 않는 상황이므로 즉시 올바르지 않은 괄호 구조라고 판단하고 false를 리턴하며 조기 종료합니다.
            if left == 0 {
                return false;
            } else {
                // 대기 중인 여는 괄호가 남아 있다면 (left > 0)
                // 방금 만난 닫는 괄호 ')'와 짝을 이루어 하나가 상쇄되므로 left 카운터를 1 감소시킵니다.
                left -= 1;
            }
        }
    }

    // 문자열 전체를 돌며 짝 맞추기 검사를 모두 끝마쳤을 때,
    // 대기 중인 여는 괄호가 하나도 없이 깔끔하게 0이 되었다면 성공적으로 올바른 괄호 형태인 것입니다.
    if left == 0 {
        return true;
    } else {
        // 여는 괄호가 여전히 남은 상태(예: "((")라면 닫히지 않았으므로 false를 리턴합니다.
        return false;
    }
}

