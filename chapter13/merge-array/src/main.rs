#![allow(clippy::all, unused)]

fn main() {
    // 프로그램 진입 시 "Hello, world!"를 콘솔에 출력합니다.
    println!("Hello, world!");
}

// 방법 1: 두 벡터를 하나로 합친 다음 정렬하여 정렬된 벡터를 반환하는 단순한 풀이입니다.
// 매개변수 a와 b는 i32 타입 원소들을 갖는 소유권(ownership)이 넘어오는 벡터(Vec)입니다.
fn solve1(a: Vec<i32>, b: Vec<i32>) -> Vec<i32> {
    // 벡터 a와 b의 요소(원소) 개수를 각각 취득합니다.
    let n = a.len();
    let m = b.len();

    // 두 벡터의 합친 원소들을 보관하기 위해 가변(mutable) 빈 벡터인 ans를 생성합니다.
    let mut ans = vec![];

    // 첫 번째 벡터 a의 모든 요소를 순회하면서 ans 벡터의 뒤에 추가합니다.
    for i in 0..n {
        ans.push(a[i]);
    }

    // 두 번째 벡터 b의 모든 요소를 순회하면서 ans 벡터의 뒤에 추가합니다.
    for i in 0..m {
        ans.push(b[i]);
    }

    // 러스트의 표준 정렬 함수인 sort()를 호출하여 ans의 모든 원소들을 오름차순으로 정렬합니다.
    ans.sort();
    
    // 최종적으로 정렬이 완료된 벡터인 ans를 반환합니다. (러스트에서는 세미콜론이 없으면 식으로 취급되어 값을 반환하게 됩니다)
    ans
}

// 방법 2: 두 벡터가 이미 오름차순으로 정렬되어 있다는 특성을 살려 투 포인터(Two Pointer) 방식으로 
// 시간 복잡도 O(N+M) 안에 선형 병합(Merge)하는 효율적인 풀이입니다.
fn solve2(a: Vec<i32>, b: Vec<i32>) -> Vec<i32> {
    // 벡터 a와 b의 크기를 계산합니다.
    let n = a.len();
    let m = b.len();

    // 병합된 정렬 결과를 저장하기 위한 빈 가변 벡터를 생성합니다.
    let mut ans = vec![];

    // a 벡터에서 현재 비교할 대상을 가리키는 인덱스(포인터) i를 정의하고 0으로 초기화합니다.
    // i에 1을 더해가면 a의 첫 번째 요소를 앞부분부터 순차적으로 처리해 제거하는 효과가 있습니다.
    // 만약 i == n이 되면 a 벡터의 모든 값을 다 처리했음을 뜻합니다.
    let mut i = 0;

    // 마찬가지로 b 벡터의 현재 비교 대상 원소를 가리킬 인덱스 j를 정의하고 0으로 초기화합니다.
    let mut j = 0;

    // a 벡터와 b 벡터의 요소를 전부 처리할 때까지 루프를 반복 수행합니다.
    // i < n이거나 j < m 이라는 조건은 두 벡터 중 최소한 하나라도 아직 미처리 원소가 남아있다는 뜻입니다.
    while i < n || j < m {
        if i == n {
            // i가 n에 도달했다는 것은 a 벡터의 원소를 모두 처리했음을 의미합니다.
            // 따라서 남은 b 벡터의 원소(b[j])를 정답 벡터 ans에 바로 추가합니다.
            ans.push(b[j]);

            // b 벡터에서 처리된 인덱스를 가리키도록 j 포인터를 1 증가시킵니다.
            j += 1;
        } else if j == m {
            // j가 m에 도달했다는 것은 b 벡터의 원소를 모두 처리했음을 의미합니다.
            // 따라서 남은 a 벡터의 원소(a[i])를 정답 벡터 ans에 바로 추가합니다.
            ans.push(a[i]);

            // a 벡터에서 처리된 인덱스를 가리키도록 i 포인터를 1 증가시킵니다.
            i += 1;
        } else if a[i] < b[j] {
            // a[i]와 b[j]를 비교하여 a[i]가 더 작다면, 오름차순 유지를 위해 
            // 더 작은 값인 a[i]를 ans에 먼저 추가합니다.
            ans.push(a[i]);

            // a 벡터에서 비교 완료된 위치를 나타내기 위해 i 포인터를 1 증가시킵니다.
            i += 1;
        } else {
            // b[j]가 a[i]보다 작거나 같은 경우에는 
            // 더 작은 값인 b[j]를 ans에 먼저 추가합니다.
            ans.push(b[j]);

            // b 벡터에서 비교 완료된 위치를 나타내기 위해 j 포인터를 1 증가시킵니다.
            j += 1;
        }
    }

    // 완전히 병합되어 정렬이 끝난 정답 벡터를 반환합니다.
    ans
}

// 단위 테스트 모듈을 설정합니다.
// #[cfg(test)]는 cargo test를 통해 테스트를 실행할 때만 이 모듈을 빌드하라는 컴파일러 지시문입니다.
#[cfg(test)]
mod tests {
    // 외부 스코프(부모 모듈)의 모든 함수(solve1, solve2 등)를 가져옵니다.
    use super::*;

    // 실제 테스트 함수로 작동하게 하는 데코레이터입니다.
    #[test]
    fn test_solve1() {
        // 이미 오름차순으로 정렬된 두 개의 입력 벡터를 solve1 함수로 합쳐 봅니다.
        let ans = solve1(vec![1, 2, 4, 5, 8], vec![2, 3, 4, 5, 6, 7]);
        // assert_eq! 매크로를 이용해 병합된 결과(ans)가 올바른 오름차순 배열과 완벽히 일치하는지 검증합니다.
        assert_eq!(ans, vec![1, 2, 2, 3, 4, 4, 5, 5, 6, 7, 8]);
    }

    #[test]
    fn test_solve2() {
        // solve2 함수에 대해서도 동일한 케이스를 테스트합니다.
        let ans = solve2(vec![1, 2, 4, 5, 8], vec![2, 3, 4, 5, 6, 7]);
        // 병합 정렬 알고리즘의 동작 결과를 assert_eq!로 검증합니다.
        assert_eq!(ans, vec![1, 2, 2, 3, 4, 4, 5, 5, 6, 7, 8]);
    }
}
